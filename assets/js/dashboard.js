          $('#history_table_wrapper').hide();
          $('#tools').hide();
          $('#table').removeAttr('hidden');
          $('#dashboardButtons').removeAttr('hidden');
          $('#dashboardTitle').removeAttr('hidden');
          $('#loader').hide();
          $("a[href*='/login']").attr("href", "/logout").text("Logout");
