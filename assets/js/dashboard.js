    // Modified createIconGrid function with lazy loading
    function createIconGrid(searchTerm = '') {
        iconContainer.innerHTML = '';
        currentIconsCount = 0;
        const filteredIcons = icons.filter(icon => 
            icon.toLowerCase().includes(searchTerm.toLowerCase())
        );
        
        loadMoreIcons(filteredIcons);

        // Add scroll event listener to the icon container
        iconContainer.addEventListener('scroll', () => {
            if (isScrollNearBottom(iconContainer) && !isLoading) {
                loadMoreIcons(filteredIcons);
            }
        });
    }

    // Helper function to check if scrolling is near bottom
    function isScrollNearBottom(element) {
        return element.scrollHeight - element.scrollTop - element.clientHeight < 50;
    }

    // Function to load more icons
    function loadMoreIcons(filteredIcons) {
        if (isLoading || currentIconsCount >= filteredIcons.length) return;

        isLoading = true;
        const fragment = document.createDocumentFragment();
        
        const endIndex = Math.min(currentIconsCount + ICONS_PER_LOAD, filteredIcons.length);
        
        for (let i = currentIconsCount; i < endIndex; i++) {
            const div = document.createElement('div');
            div.className = 'icon-item';
            const cleanIconClass = filteredIcons[i].replace(/^fas\s+fas\s+/, 'fas ');
            const iconName = cleanIconClass.replace(/^fas fa-/, '');
            div.innerHTML = `
                <i class="${cleanIconClass}"></i>
                <small>${iconName}</small>
            `;
            div.onclick = () => {
                handleIconClick(cleanIconClass);
            };
            fragment.appendChild(div);
        }

        iconContainer.appendChild(fragment);
        currentIconsCount = endIndex;
        isLoading = false;
    }

